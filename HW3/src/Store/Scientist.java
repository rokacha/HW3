package Store;

/**
 *
 */
public class Scientist {
	private String name;
	private String spec;
	private int cost;
	
	
	
	public Scientist(String name, String spec, int cost) {
		this.name = name;
		this.spec = spec;
		this.cost = cost;
	}
	
	public String getName() {
		return name;
	}
	public String getSpec() {
		return spec;
	}
	public int getCost() {
		return cost;
	}
	


}
